namespace betterauth\utils;

use pocketmine\Player;

final class SystemUtils
{

    /**
     * @param mixed $var
     * @return boolean
     */
    public static function isValidPlayer($var) : bool
    {
        return ($var instanceof Player && $var->isOnline());
    }
}